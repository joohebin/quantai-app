# 针对每一处 \\\\'...\\\\ 进行修复
# 模式: 'something onclick="fn(\\'' + varname + '\\')' more'
# 需要改成: `something onclick="fn('${varname}')' more`
#
# 但这样改需要把整段字符串拼接都改成模板字符串，范围可能很大
#
# 更简单的办法：把 onclick 里的 \\'' 改为 \\x27 (单引号的JS十六进制转义)
# \\x27 在 JS 字符串里是单引号，且不会引起解析器困惑
# 原: onclick="fn(\\'' + id + '\\')"
# 改: onclick="fn(\\x27' + id + '\\x27)"
# 不对，这样在 HTML onclick 里会解析为字面 \x27 而不是引号
#
# 最根本的修复：把有问题的字符串拼接都换成模板字符串
# 
# 因为这11处分散在不同函数里，最干净的方式是用正则替换
# 把每一行的字符串拼接模式转换为模板字符串
#
# 模式分析（以L3451为例）:
# ' ... onclick="sqLike(\\'' + p.id + '\\')"...'
# 文件中实际是（Python repr）:
# "onclick=\"sqLike(\\\\'' + p.id + '\\\\')\""
# 
# 实际文件字节（JS源码中）:
# onclick="sqLike(\\'' + p.id + '\\')"
# 当JS执行字符串拼接后等于:
# onclick="sqLike('p.id值')"  <- 这是正确的HTML
#
# 但浏览器解析 JS 源码时:
# 单引号字符串 '...onclick="sqLike(\\'' 
# \\' 是转义单引号 -> 字符 '
# 但接着 '  -> 又是单引号，字符串结束了！
# 然后 + p.id + 是正常表达式
# 然后 '\\')' 又是新字符串
# 所以实际上 JS 解析器是: 字符串 '...sqLike(\'' 拼接 + p.id + '\')' 等等
# 这其实是合法的！字符串 '...\\'' 中 \\' 是转义单引号，字符串正常继续
# 
# 等等，重新分析：
# JavaScript 字符串里 \\' 是 \ 然后 ' 
# 不是转义，\\ 是转义反斜杠（得到 \），然后 ' 结束字符串
# 所以 '...(\\'' 中：
#   ...(\\  -> 字符串内容包含字面 \
#   ''  -> 第一个 ' 结束字符串，第二个 ' 开始新字符串（空字符串）
# 然后接着 + p.id + 
# 然后 '\\' -> 字符串内容是 \，)' 然后 ) 在字符串里，然后 ' 结束

# 所以实际上：
# '<button onclick="sqLike(\\'' + p.id + '\\')">'
# 解析为：
# '<button onclick="sqLike(\' + '' + p.id + '\' + ')">'
# = '<button onclick="sqLike(\'' + p.id + '\')">'
# 实际拼接结果：'<button onclick="sqLike(\'p.id值\')">'
# 这样 onclick 里的引号是被 \ 转义的单引号... 但 HTML onclick 里 \" 转义是 "，没有 \'
# 所以这里用 \' 在 HTML onclick 中是无效的
# 
# 真正的正确写法应该是：
# '<button onclick="sqLike(\'' + p.id + '\')">'  <- 用双引号包 onclick，内部无需转义单引号
# 或者直接：
# `<button onclick="sqLike('${p.id}')">`  <- 模板字符串，最干净

# 结论：这些代码逻辑上能工作（虽然转义混乱），Node.js vm.Script 解析失败
# 但浏览器能解析！！！
# 
# 关键问题：浏览器是否真的报了语法错误？还是其他运行时错误？
# 图片里显示 showPage:undefined，不是 SyntaxError。
# 这意味着整个 script 在解析阶段就失败了，后续所有 function 都没定义。
# 
# 所以两种可能：
# 1. 浏览器也解析失败（严格模式？某些特殊字符？）
# 2. 另有其他问题导致 showPage 找不到
#
# 验证：浏览器对这些 \\'' 的处理
# 答案：现代 Chrome/FF 对 '..\\'' 的处理：
# \\ -> 字面反斜杠
# '' -> 结束字符串然后空字符串
# 这是合法JS！
# 但有时候这些空字符串拼接会让后续代码解析失败
# 
# 无论如何，把这11处全部换成模板字符串是最安全的修法

# ========================================================
# 直接用脚本对11处进行逐行修复
# ========================================================

lines = content.split('\n')
fixed_lines = []
fix_count = 0

for i, line in enumerate(lines):
    if "\\\\'" not in line:
        fixed_lines.append(line)
        continue
    
    original = line
    # 对这一行做修复
    # 思路：整行如果包含 \\\\ 模式的字符串拼接，尝试转换为模板字符串
    # 找出模式: ' ... onclick="fn(\\'' + var + '\\')"...'
    # 转换为:   ` ... onclick="fn('${var}')"...`
    #
    # 用正则匹配：'prefix onclick="fn(\\'' + ([\w.]+) + '\\')' suffix'
    # 这个正则很复杂，改用固定模式替换
    
    # 模式1: \\\\'  followed by  ' + varname + '\\')  (at end)
    # 在Python字符串中: "\\\\'" matches the literal characters \\' in the file
    
    # 简单做法：把 (\\\\'' + ([\w.]+) + '\\\\\\'\\) 替换为 (\\'${\\1}\\')
    # 不，太复杂。改用更直接的方法：
    
    # 替换 \\'id\\' -> \x27id\x27 不行
    # 替换为直接用单引号但确保字符串不中断
    
    # 实际上：对于 onclick 里的单引号，用 &apos; HTML实体是最安全的
    # onclick="fn(&apos;xxx&apos;)"  这在onclick里是有效的
    
    # 但已有的代码太多，最干净的还是模板字符串
    # 
    # 实际上测试一下：浏览器对 '...(\\' + id + \\')'  能正确解析吗？
    # \\  = \ (字面反斜杠字符)
    # '   = 结束单引号字符串
    # + id + = JS表达式
    # '\\  = 开始新的单引号字符串，\\ = 字面反斜杠
    # )   = 字符串内容
    # '   = 结束字符串
    # 结果：'...(\\' + id + '\\')'
    # = '...(\' + id  (注意：\' 在单引号字符串外面是啥？)
    # 啊不对，重新分析:
    # JS 中 \\ 在字符串里是转义，得到字符 \
    # 字符串 '...(\\' : ... ( \ -> 然后 ' 结束字符串
    # 接着 + id + ，合法
    # 然后 '\\)' : \\ -> \ , ) -> ) , ' -> 结束
    # 整体: '...(\' + id + '\)'
    # 拼接后: ...(\ + id的值 + \)
    # 即: onclick="sqLike(\p.id值\)" <- 错的！
    # 
    # 正确的 JS 应该是：
    # '<button onclick="sqLike(\'' + p.id + '\')">' 
    # 字符串 '..sqLike(\'' : .. sqLike( \' -> \' 是转义的单引号（字符 '）
    #   然后 '  -> 又一个 ' : 这个 ' 结束字符串！！
    # 
    # 所以 \'' 实际上是：\' (转义单引号) 然后 ' (结束字符串)，再拼接
    # 结果是: ..sqLike(' + p.id + ')...  OK！
    # 
    # 在浏览器里 '..sqLike(\\'' 是：
    # \\ = 反斜杠字符
    # ' = 结束字符串
    # 结果是: ..sqLike(\  + p.id + \)  <- 仍然是 \ 不是 '
    # 
    # ！！！这段代码在功能上就是错的！生成的HTML onclick是 sqLike(\id\) 而不是 sqLike('id')
    # 这会导致运行时错误但不是语法错误
    # 
    # 修复：把 \\\\' 改为 \\' (让JS字符串里有真正的单引号转义)
    # 在文件里 \\' 是 \ '，在JS执行后字符串里是 ' (单引号)
    
    # 原文件: onclick="fn(\\'' + id + '\\')"
    # 修复后: onclick="fn(\\'' + id + '\\')"
    # 不对，原来就是这样。。。
    
    # 好，彻底想清楚：
    # 文件里（原始字符）: \\\\'
    # Python repr的 \\\\ 代表文件里的两个字符 \\
    # Python repr的 ' 代表文件里的一个字符 '
    # 所以文件里是 \\' 三个字符? 不对
    # Python repr里的 "\\\\\\'": 
    #   \\\\ -> 文件里 \\  (两个反斜杠)
    #   \\' -> 文件里 \'  (一个反斜杠+引号)
    # 所以 "\\\\\\''" = 文件里 \\'' (两个反斜杠+两个引号)
    # 
    # 在JS源码里字符串内部 \\'' 是：
    #   \\ -> 转义得到字面 \
    #   '' -> 结束字符串（第一个 '），然后开始空字符串（第二个 '）
    # 
    # 所以我之前的分析是对的：\\'' 在JS里是 字面\ 然后拼接符号结束字符串
    # 
    # 所以 '..onclick="fn(\\'' + p.id + '\\')..."' 解析为:
    # 字符串1: '..onclick="fn(\\'  = ..onclick="fn(\
    # 拼接 p.id
    # 字符串2: '\\')'                 = \)
    # 结果: ..onclick="fn(\" + p.id + "\)"  
    # 即 onclick="fn(\xxx\)"  <- 不是预期的 fn('xxx')
    # 
    # 结论: 原来的代码在功能上就是错的，但浏览器可能还是执行了（错误地）
    # 正确写法应该是: '<button onclick="fn(\'' + id + '\')">'
    # 但单引号字符串里 \' 在某些解析中行为不一致...
    # 
    # 最清晰无歧义的写法: 用双引号包字符串或者用模板字符串
    # 
    # 终极修复方案：全部改为模板字符串

    # 尝试把当前行的字符串拼接改为模板字符串（简化正则）
    # 检测当前行是否有 \\\\' (两个反斜杠+单引号)
    new_line = line
    
    # 这里不做自动转换，太容易出错。记录需要手工改的行并报告
    print(f'L{i+1}: NEEDS MANUAL FIX: {line[:160]}')
    fix_count += 1
    fixed_lines.append(line)

